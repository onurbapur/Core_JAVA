package Day25_StringClass2;

public class stringContains {
    public static void main(String[] args) {
        String myStr ="Hello";
        System.out.println("myStr.contains(\"Hel\") = " + myStr.contains("Hel"));
        System.out.println("myStr.contains(\"world\") = " + myStr.contains("world"));
        System.out.println("myStr.contains(\"lo\") = " + myStr.contains("lo"));
        System.out.println("myStr.contains(\"hel\") = " + myStr.contains("hel"));

    }

}
