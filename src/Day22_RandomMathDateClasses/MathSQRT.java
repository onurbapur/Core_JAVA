package Day22_RandomMathDateClasses;

public class MathSQRT {
    public static void main(String[] args) {
        System.out.println("Square root.");

        System.out.println("Math.sqrt(4) = " + Math.sqrt(4));
        System.out.println("Math.pow(2,2) = " + Math.pow(2, 2)); //2 * 2
        System.out.println("Math.pow(2,3) = " + Math.pow(2, 3)); //2 * 2 * 2
        System.out.println("Math.pow(2,100) = " + Math.pow(2, 8));

        System.out.println("Math.min(123,34534) = " + Math.min(123, 34534));
    }
}
