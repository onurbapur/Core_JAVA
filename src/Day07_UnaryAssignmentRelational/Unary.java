package Day07_UnaryAssignmentRelational;

public class Unary {
    public static void main(String[] args) {
        System.out.println("Unary operators...");
        int num = 10;
        System.out.println("num...: " + num);

        num++;  //num = num + 1;
        System.out.println("num(unary) num++...: " + num);

        num--; //num = num - 1;
        System.out.println("num(unary) num--...: " + num);
    }

}
