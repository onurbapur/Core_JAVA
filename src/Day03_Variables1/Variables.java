package Day03_Variables1;

public class Variables {
    public static void main(String[] args) {
        // Değişkenler ve veri tiplerine ilişkin örnek uygulama....

        System.out.println("Değişkenler ve Veri tipleri...");
        System.out.println("------------------------------");

        boolean evliMi = false;
        System.out.println(evliMi);

        byte siraNo = 121; //byte(tam sayı) veri tipi
        System.out.println(siraNo);

        int abc = 1234578906; // integer(tam sayı) veri tipi
        System.out.println(abc);

        long okulNumarasi = 1234576492874029365L; // Long(tam sayı) veri tipi
        System.out.println(okulNumarasi);

        float kdvOrani = 0.187890765423612461416472468724687216F; // Float ondalikli veri tipi
        System.out.println(kdvOrani);

        // Double ondalikli veri tipi
        double piSayisi = 3.14;
        System.out.println(piSayisi);

        double okulNo = 1; // Gereksiz büyüklükte bir veri tipi seçimi
        System.out.println(piSayisi);

        double coookBuuuyukBirSayi = 123456789746246174618418701234567890.2134235346567624235645871215634265367;
        System.out.println(coookBuuuyukBirSayi);



    }
}
